<h2>Web Development</h2>
<p>
  I've been working as a Frontend Developer since 2019, and gained many skills and
  experiences along the way. My motivation since I started looking up to programming as a
  career since the early 2000s, was how much it enables me to solve almost any problem.
</p>
<p>
  I have worked in retail, hospitals, and human resources, and in almost all of them, I've
  been the one to build the tools that helped me and my team to do our jobs better, once I
