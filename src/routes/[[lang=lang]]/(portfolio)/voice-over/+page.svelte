<h1>Voice-Over Portfolio</h1>
<p>
  I started working as a voice-over artist in 2019, when I provided a gig to a friend of
  mine who ran an advertising agency.
</p>
<p>
  Since then, it had dawned on me that I could make a living out of it. Many gigs came
  after that, and the whole thing shifted into a real interest and enthusiasm, that I took
  a course on the art, which led me to being selected for the voice-acting industry, where
  I've been enjoying it the most.
</p>
<h2>Audiohat</h2>
<p>
  Audiohat is a voice-over agency that I'm a part of, and I'm very proud of it. They
  provide radio drama, both original works and from the public domain, as well as
